    /**
     * @param pNum 默认值为1
     */
    public static Bitmap navieRemoveNoise(Bitmap bitmap, int pNum) {
        final int BLACK = 0;
        final int WHITE = 255;

        int i, j, m, n, nValue, nCount;
        int nWidth = bitmap.getWidth();
        int nHeight = bitmap.getHeight();

        Bitmap bitmap2;
        bitmap2 = bitmap.copy(Bitmap.Config.ARGB_8888, true);

        // 对图像的边缘进行预处理
        for (i = 0; i < nWidth; ++i) {
            bitmap2.setPixel(0, i,  WHITE);
            bitmap2.setPixel(nHeight - 1, i, WHITE);
        }

        for (i = 0; i < nHeight; ++i) {
            bitmap2.setPixel(i, 0, WHITE);
            bitmap2.setPixel(i, nWidth - 1,  WHITE);
        }

        // 如果一个点的周围都是白色的，而它确是黑色的，删除它
        for (j = 1; j < nHeight - 1; ++j) {
            for (i = 1; i < nWidth - 1; ++i) {
                Trace.e("ij", "i: " + i + "\nj: " + j);
                nValue = bitmap2.getPixel(i, j);
                if (nValue == 0) {
                    nCount = 0;
                    // 比较以(j ,i)为中心的9宫格，如果周围都是白色的，同化
                    for (m = j - 1; m <= j + 1; ++m) {
                        for (n = i - 1; n <= i + 1; ++n) {
                            Trace.e("mn", "m: " + m + "\nn: " + n);
                            if (bitmap2.getPixel(n, m) == 0) {
                                nCount++;
                            }
                        }
                    }
                    if (nCount <= pNum) {
                        // 周围黑色点的个数小于阀值pNum,把该点设置白色
                        bitmap2.setPixel(i, j, WHITE);
                    }
                } else {
                    nCount = 0;
                    // 比较以(j ,i)为中心的9宫格，如果周围都是黑色的，同化
                    for (m = j - 1; m <= j + 1; ++m) {
                        for (n = i - 1; n <= i + 1; ++n) {
                            if (bitmap2.getPixel(n, m) == 0) {
                                nCount++;
                            }
                        }
                    }
                    if (nCount >= 7) {
                        // 周围黑色点的个数大于等于7,把该点设置黑色;即周围都是黑色
                        bitmap2.setPixel(i, j, BLACK);
                    }
                }
            }
        }
        return bitmap2;
    }

    /**
     * 是否黑色
     *
     * @param
     *
     * */
    public static boolean isBlack(Bitmap bitmap22) {
        // 获取图片的宽和高
        int width = bitmap22.getWidth();
        int height = bitmap22.getHeight();
        // 创建二值化图像
        Bitmap bitmap;
        bitmap = bitmap22.copy(Bitmap.Config.ARGB_8888, true);
        // 遍历原始图像像素,并进行二值化处理
        for (int i = 0; i < width; i++) {
            for (int j = 0; j < height; j++) {
                // 得到当前的像素值
                int pixel = bitmap.getPixel(i, j);
                if(pixel != -16777216)
                    return false;
            }
        }
        return true;
    }
